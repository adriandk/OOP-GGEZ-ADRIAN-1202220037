public class Hewan {
    
    protected int kaki;
    protected String reproduksi;
    protected String makanan;
    protected String nama;

    public Hewan(int kakiHewan, String caraReporduksi, String jenisMakanan, String namaHewan){
        this.kaki = kakiHewan;
        this.reproduksi = caraReporduksi;
        this.makanan = jenisMakanan;
        this.nama = namaHewan;
    }
}
